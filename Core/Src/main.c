void servo_write(int angle)
{
	htim5.Instance->CCR1 = map(0,180,50,250,angle);
}

void servo_sweep(void)
{
		for(int i = 0; i <= 180; i++)
		{
			servo_write(i);
			HAL_Delay(10);
		}
		for(int i = 180; i >= 0; i--)
		{
			servo_write(i);
			HAL_Delay(10);
		}
}

int map(int st1, int fn1, int st2, int fn2, int value)
{
    return (1.0*(value-st1))/((fn1-st1)*1.0) * (fn2-st2)+st2;
}

